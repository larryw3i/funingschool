class App:
    def __init__(self):
        from fnschool.path import app_config_fpath

        self.cfg_fpath = app_config_fpath
        self._name = None
        self._version = None
        self._cfg = None
        self._ui = None
        self._use_tk = False
        pass

    @use_tk.setter
    def use_tk(value = True):
        self._use_tk = value
    
    @property
    def use_tk(self):
        return self._use_tk
        pass

    @property
    def name(self):
        if not self._name:
            self._name = app_name
        return self._name
        pass
    
    @property
    def version(self):
        if not self._version:
            self._version = get_app_version()
        return self._version
        pass

    @property
    def ui(self):
        if not self._ui:
            from fnschool.ui import UI

            self._ui = UI()
            pass
        return self._ui
        pass

    @property
    def cfg(self):
        from fnschool.config import AppConfig

        if not self._cfg:
            self._cfg = AppConfig(self.cfg_fpath)
        return self._cfg
        pass

    pass


